/******************Select para comprobar los mensjes que se han enviado por chat entre amigos.
				   En el caso de que los usuarios no sean el select no devolverá nada*********************/
SELECT Users.users_id_user,Users.users_username,Chats.chats_text FROM Users
INNER JOIN Chats ON Chats.Users_users_id_user_receive=Users.users_id_user
INNER JOIN Friends ON Friends.Users_users_id_user=Users.users_id_user
INNER JOIN Friends as Friends2 ON Friends2.Users_users_id_user1 = Users.users_id_user
ORDER BY Chats.chats_id_chat

SELECT Users.users_username,Games.games_name,Games.games_price,Bills.bills_date,Bills.bills_price,Bills.bills_payment,Bills.bills_id_bill FROM Bills
INNER JOIN Users ON Users.users_id_user=Bills.Users_users_id_user
INNER JOIN Bill_Details ON Bill_Details.Bills_bills_id_bill=Bills.bills_id_bill
INNER JOIN Games ON Games.games_id_game=Bill_Details.Games_games_id_game;
/******************Select para comprobar la biblioteca de todos los usuarios******************/
SELECT Users.users_username,Libraries.libraries_id_library,Games.games_name FROM Games_libraries
INNER JOIN Libraries ON Libraries.libraries_id_library=Games_libraries.Libraries_libraries_id_library
INNER JOIN Users ON Users.users_id_user=Libraries.Users_users_id_user
INNER JOIN Games ON Games.games_id_game=Games_libraries.Games_games_id_game
ORDER BY Users.users_username,Games.games_name;
/******************Select para comprobar todas las reviews hechas por los usuarios******************/
SELECT Users.users_username,Games.games_name,Reviews.reviews_text,Reviews.reviews_rating FROM Reviews
INNER JOIN Games ON Games.games_id_game=Reviews.Games_games_id_game
INNER JOIN Users ON Users.users_id_user=Reviews.Users_users_id_user;
/******************Select para comprobar todas los topics creados por los usuarios******************/
SELECT Users.users_username,Games.games_name,Topics.topics_title,Topics.topics_text FROM Games
INNER JOIN Forums ON Forums.Games_games_id_game=Games.games_id_game
INNER JOIN Topics ON Topics.Forums_forums_id_forum=Forums.forums_id_forum
INNER JOIN Users ON Users.users_id_user=Topics.Users_users_id_user;

